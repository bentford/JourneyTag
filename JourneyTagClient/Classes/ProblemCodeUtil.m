#import "ProblemCodeUtil.h"


@implementation ProblemCodeUtil

+ (NSString*)problemCodeToString:(int)problemCode
{
    switch (problemCode) {
        case 1:
            return @"Network Reception ";
            break;
        case 2:
            return @"Not Reachable";
            break;
        case 3:
            return @"GPS Interference";
            break;
        default:
            return @"";
            break;
    }
}
@end
